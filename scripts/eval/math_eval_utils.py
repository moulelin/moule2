def _extract_answer_value(raw):
    """Extract answer value from raw text after 'the answer is' / 'final answer is'.

    Strategy:
      1. If it starts with a number, grab just the number (handles '6 hours. Therefore...')
      2. Otherwise keep the math expression and truncate at the first sentence boundary
         (handles '(3, 5). However...' and '\\frac{1}{2}. But...')
    """
    # Try numeric first
    m = re.match(r"\$?(-?\d[\d,]*\.?\d*)", raw)
    if m:
        return m.group(1).replace(",", "")
    # Non-numeric (latex, tuple, etc.): truncate at sentence boundary
    # Split where a non-letter char is followed by whitespace + Capitalized word
    parts = re.split(r"(?<=[^a-zA-Z])\s+(?=[A-Z][a-z])", raw)
    return parts[0].strip()


def extract_answer(pred_str, use_last_number=True):
    """Extract the answer from a model prediction string."""
    # Strip thinking block: closed <think>...</think> or unclosed <think>... (truncated)
    pred_str = re.sub(r"<think>.*?</think>", "", pred_str, flags=re.DOTALL).strip()
    pred_str = re.sub(r"<think>.*", "", pred_str, flags=re.DOTALL).strip()
    pred_str = pred_str.replace("\u043a\u0438", "")

    if "boxed" in pred_str:
        ans = pred_str.split("boxed")[-1]
        if len(ans) == 0:
            return ""
        elif ans[0] == "{":
            stack = 1
            a = ""
            for c in ans[1:]:
                if c == "{":
                    stack += 1
                    a += c
                elif c == "}":
                    stack -= 1
                    if stack == 0:
                        break
                    a += c
                else:
                    a += c
        else:
            a = ans.split("$")[0].strip()
        pred = a
    elif "final answer is $" in pred_str and "$. I hope" in pred_str:
        tmp = pred_str.split("final answer is $", 1)[1]
        pred = tmp.split("$. I hope", 1)[0].strip()
    elif "the answer is" in pred_str:
        raw = pred_str.split("the answer is")[-1].strip().split("\n")[0].strip()
        raw = re.sub(r"^[\s:]+", "", raw)
        pred = _extract_answer_value(raw)
    elif "final answer is" in pred_str:
        raw = pred_str.split("final answer is")[-1].strip().split("\n")[0].strip()
        raw = re.sub(r"^[\s:]+", "", raw)
        pred = _extract_answer_value(raw)
    else:
        if use_last_number:
            pattern = r"-?\d*\.?\d+"
            pred = re.findall(pattern, pred_str.replace(",", ""))
            if len(pred) >= 1:
                pred = pred[-1]
            else:
                pred = ""
        else:
            pred = ""

    # multiple line
    pred = re.sub(r"\n\s*", "", pred)
    if pred != "" and pred[0] == ":":
        pred = pred[1:]
    if pred != "" and pred[-1] == ".":
        pred = pred[:-1]
    if pred != "" and pred[-1] == "/":
        pred = pred[:-1]
    pred = strip_string(pred)
    return pred


